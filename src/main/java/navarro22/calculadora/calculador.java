    private void botonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIgualActionPerformed
        double sin=Double.parseDouble(digito);
            double cos=Double.parseDouble(digito);
            double log=Double.parseDouble(digito);
            double raiz=Double.parseDouble(digito);
            raizCuadrada=Math.sqrt(raiz);
            seno= Math.sin(sin);
            coseno=Math.cos(cos);
            loga=Math.log(log);
            labelResultadoRaiz.setText(String.format("%.4f",raizCuadrada));
            labelResultadoSeno.setText(String.format("%.4f",seno));
            labelResultadoCoseno.setText(String.format("%.4f",coseno));
            labelResultadoLog.setText(String.format("%.4f",loga));
    }//GEN-LAST:event_botonIgualActionPerformed
